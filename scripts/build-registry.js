#!/usr/bin/env node

/**
 * Build script that generates .claude-plugin/marketplace.json from all plugin manifests
 * Run with: node scripts/build-registry.js
 */

import { readdir, readFile, writeFile, mkdir } from "fs/promises";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = join(__dirname, "..");
const PLUGINS_DIR = join(ROOT_DIR, "plugins");
const OUTPUT_DIR = join(ROOT_DIR, ".claude-plugin");

async function tryReadManifest(paths) {
  for (const p of paths) {
    try {
      const content = await readFile(p, "utf-8");
      return JSON.parse(content);
    } catch {
      continue;
    }
  }
  return null;
}

async function findPlugins() {
  const plugins = [];

  let entries;
  try {
    entries = await readdir(PLUGINS_DIR, { withFileTypes: true });
  } catch {
    console.log("No plugins directory found");
    return plugins;
  }

  // Scan top-level directories under plugins/ (category dirs like skills/, hooks/, mcp-servers/)
  // and also direct plugin dirs that contain .claude-plugin/plugin.json
  for (const entry of entries) {
    if (!entry.isDirectory()) continue;

    // Check if this is a direct plugin dir (has manifest.json or .claude-plugin/plugin.json)
    const directManifest = await tryReadManifest([
      join(PLUGINS_DIR, entry.name, "manifest.json"),
      join(PLUGINS_DIR, entry.name, ".claude-plugin", "plugin.json"),
    ]);
    if (directManifest) {
      plugins.push({ ...directManifest, path: `plugins/${entry.name}` });
      console.log(`Found: ${directManifest.name}`);
      continue;
    }

    // Scan as category directory
    const categoryDir = join(PLUGINS_DIR, entry.name);
    let subEntries;
    try {
      subEntries = await readdir(categoryDir, { withFileTypes: true });
    } catch {
      continue;
    }

    for (const subEntry of subEntries) {
      if (!subEntry.isDirectory()) continue;

      const manifest = await tryReadManifest([
        join(categoryDir, subEntry.name, "manifest.json"),
        join(categoryDir, subEntry.name, ".claude-plugin", "plugin.json"),
      ]);
      if (manifest) {
        plugins.push({ ...manifest, path: `plugins/${entry.name}/${subEntry.name}` });
        console.log(`Found: ${manifest.name}`);
      }
    }
  }

  return plugins;
}

function generatePluginTable(plugins) {
  // Group plugins by category (derived from path: plugins/<category>/<name>)
  const groups = {};
  for (const plugin of plugins) {
    const category = plugin.path.split("/")[1] || "other";
    if (!groups[category]) groups[category] = [];
    groups[category].push(plugin);
  }

  // Sort categories, then plugins within each category
  const categoryOrder = Object.keys(groups).sort();
  const lines = ["## Available Plugins", ""];

  for (const category of categoryOrder) {
    const label = category.charAt(0).toUpperCase() + category.slice(1);
    lines.push(`### ${label}`, "");
    lines.push("| Plugin | Description |");
    lines.push("|--------|-------------|");

    const sorted = groups[category].sort((a, b) => a.name.localeCompare(b.name));
    for (const p of sorted) {
      // Truncate to first sentence for the table
      const desc = p.description.split(/\.\s/)[0].replace(/\.$/, "") + ".";
      lines.push(`| **${p.name}** | ${desc} |`);
    }
    lines.push("");
  }

  return lines.join("\n").trimEnd();
}

const START_MARKER = "<!-- PLUGINS:START - auto-generated by npm run build:registry, do not edit manually -->";
const END_MARKER = "<!-- PLUGINS:END -->";

async function updateReadme(plugins) {
  const readmePath = join(ROOT_DIR, "README.md");
  let readme;
  try {
    readme = await readFile(readmePath, "utf-8");
  } catch {
    console.log("No README.md found, skipping plugin table update");
    return;
  }

  const startIdx = readme.indexOf(START_MARKER);
  const endIdx = readme.indexOf(END_MARKER);
  if (startIdx === -1 || endIdx === -1) {
    console.log("No PLUGINS markers found in README.md, skipping plugin table update");
    return;
  }

  const before = readme.slice(0, startIdx);
  const after = readme.slice(endIdx + END_MARKER.length);
  const table = generatePluginTable(plugins);
  const updated = before + START_MARKER + "\n" + table + "\n" + END_MARKER + after;

  await writeFile(readmePath, updated);
  console.log("Updated README.md plugin table");
}

async function buildRegistry() {
  console.log("Building marketplace.json...\n");

  const plugins = await findPlugins();

  const marketplace = {
    $schema: "https://anthropic.com/claude-code/marketplace.schema.json",
    name: "hl-mp",
    description: "Plugins for Claude Code",
    owner: {
      name: "hl",
    },
    plugins: plugins.map((plugin) => {
      const entry = {
        name: plugin.name,
        source: `./${plugin.path}`,
        description: plugin.description,
      };
      if (plugin.version) entry.version = plugin.version;
      if (plugin.author?.name) entry.author = { name: plugin.author.name };
      if (plugin.category) entry.category = plugin.category;
      return entry;
    }),
  };

  await mkdir(OUTPUT_DIR, { recursive: true });
  const outputPath = join(OUTPUT_DIR, "marketplace.json");
  await writeFile(outputPath, JSON.stringify(marketplace, null, 2) + "\n");

  console.log(`\nGenerated .claude-plugin/marketplace.json with ${plugins.length} plugin(s)`);

  await updateReadme(plugins);
}

buildRegistry().catch(console.error);
